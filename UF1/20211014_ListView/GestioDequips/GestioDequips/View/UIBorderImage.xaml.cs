namespace GestioDequips.View
{
    public sealed partial class UIBorderImage : UserControl
    {
        public UIBorderImage()
        {
            this.InitializeComponent();



            
        }



        public ImageSource Source
        {
            get { return (ImageSource)GetValue(SourceProperty); }
            set { SetValue(SourceProperty, value); }
        }

        // Using a DependencyProperty as the backing store for Source.  This enables animation, styling, binding, etc...
        public static readonly DependencyProperty SourceProperty =
            DependencyProperty.Register("Source", typeof(ImageSource), typeof(UIBorderImage), new PropertyMetadata(null));



    }
}
